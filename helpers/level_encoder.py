def encode_lvl(src):
    result = []
    current = src[0]
    current_cnt = 1
    for ind, num in enumerate(src):
        if ind == 0:
            continue
        if num == current:
            current_cnt += 1
        else:
            result += [current_cnt, current]
            current = num
            current_cnt = 1
    result += [current_cnt, current]
    return result


